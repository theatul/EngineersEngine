<ul type="circle">
<li>
<p>We strongly recommend that authors submitting to OEBI copy and paste articles from a basic text editor like Notepad or NoteTab Light. Following this suggestion will ensure that paragraph breaks, line length and other basic formatting will be properly handled by our submission form. Copying and pasting content from applications like MS Word can and does result in a variety of formatting problems. The reason for this is that MS Word and similar applications use non-ASCII, sometimes invisible, characters that are copied with article content into our submission form, causing articles to appear very differently from the way they were submitted.</p></li>
<li>
<p>
Keeping the above in mind, it is not necessary for authors to use html tags for specifying font size or to create paragraph and line breaks. Use of HTML should be minimal and we suggest html tags only be used for creating live links to sites, services or products. Any link that begins with "http://" will automatically be clickable. For example, "http://www.goarticles.com" would appear as a hyperlink. To hyperlink a word or phrase, HTML anchor tags need to be placed around that word or phrase.</p></li>

<li>
</p>
<p>OEBI's policy prohibits the use of copyrighted material in a manner that violates the copyright owner's rights. In general, this means that authors should only submit their own original written work to which they have natural copyright.
In cases where copyright is disputed (usually as a result of plagiarism or unauthorized copying), articles will be immediately removed without recourse or discussion, regardless of the arguments presented by the disputing parties.
Authors who are found to be in breach of copyright will be banned and all of their submitted content removed. 
</p></li>
<li>
<p>Article submissions to OEBI are free and new additions to our database are indexed once a day, roughly every 24 hours. This means that any article you submit will be searchable by, and available to, visitors the day after it is submitted. Before submitting an article, please search the OEBI database by author name or article title to ensure your articles have not already been added by someone else.</p></li>
<li>
<p>We strongly recommend that you do not submit articles that are not your own unless the author has given you permission to do so.</p></li>

<li>
<p>Submit your article to one category only. Submissions to multiple categories are automatically removed on a daily basis.</p></li>

<li>
<p>Submissions that are commercial or advertorial in content will be removed from the OEBI database. OEBI is a free-content directory not a forum for advertising commercial products, services or programs.</p></li>

<li>
<p>Submissions that are less than 200 words will be removed from the OEBI database. Longer articles may also be removed if they contain no useful content or are submitted for the sole purpose of obtaining backlinks or promoting a product or service.</p></li>

<li>
<p>Submissions that consist of a few lines of text with a link pointing to another site where the article supposedly resides will be removed.</p></li>

<li>
<p>Article submissions that are plagiarized or in violation of another author's copyright will be removed. Read the section below for more information on our copyright policy. </p></li>

<li>
<p>Article submissions that contain or reference adult or pornographic content; that are inflammatory, offensive or promote hate literature; that promote illegal activities or that are in violation of another person's, or company's, rights or trademarks; will be removed. </p></li>
<li>
<p>Note: OEBI reserves the right to remove any article or articles for any reason with or without notice or explanation. In cases where the above or other guidelines are severely abused, so-called authors will be banned.</p>
</li>
	